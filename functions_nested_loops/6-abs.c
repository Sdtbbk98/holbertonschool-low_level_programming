/**
 *_abs -it calculate the absolute value of an integer
 *
 * @num: input number
 *
 * Return: the absolute(the positibe number) value of @num
 */

int _abs(int num)
{

	if (num < 0)
	{
		num = num * (-1);
		return (num);
	}
	else if (num == 0)
	{
		return (num);
	}
	else
	{
		return (num);
	}
}

